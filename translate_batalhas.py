def translate_seed(seed, idx):
    """Add EN and ES translations to a seed."""
    s = seed.copy()
    semente = seed.get('semente', '')
    pergunta = seed.get('pergunta', '')
    opcoes = seed.get('opcoes', [])

    # We'll use the pre-built translation map
    if idx in TRANSLATIONS:
        t = TRANSLATIONS[idx]
        s['semente_en'] = t.get('semente_en', auto_translate_semente_en(semente))
        s['pergunta_en'] = t.get('pergunta_en', '')
        s['opcoes_en'] = t.get('opcoes_en', opcoes)
        s['semente_es'] = t.get('semente_es', auto_translate_semente_es(semente))
        s['pergunta_es'] = t.get('pergunta_es', '')
        s['opcoes_es'] = t.get('opcoes_es', opcoes)
    else:
        # Auto-generate based on topic patterns
        semente_en, pergunta_en, opcoes_en = auto_translate_en(semente, pergunta, opcoes)
        semente_es, pergunta_es, opcoes_es = auto_translate_es(semente, pergunta, opcoes)
        s['semente_en'] = semente_en
        s['pergunta_en'] = pergunta_en
        s['opcoes_en'] = opcoes_en
        s['semente_es'] = semente_es
        s['pergunta_es'] = pergunta_es
        s['opcoes_es'] = opcoes_es
    return s


